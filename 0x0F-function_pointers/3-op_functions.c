#include <stdlib.h>
#include <stdio.h>
#include "3-calc.h"
/**
 * op_add - a function that performs an addition
 * @a: 1st integer
 * @b: 2nd integer
 * Return: 0
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - a function that performs a substraction operation
 * @a: 1st integer
 * @b: 2nd integer
 * Return: 0
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - a function that performs a multiplication operation
 * @a: input one
 * @b: input two
 * Return: 0
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - a functions that divides 2 integers
 * @a: 1st integer
 * @b: 2nd integer
 * Return: 0
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - a function that returns the remainder of a division
 * @a: 1st integer
 * @b: 2nd integer
 * Return: 0
 */
int op_mod(int a, int b)
{
return (a % b);
}
